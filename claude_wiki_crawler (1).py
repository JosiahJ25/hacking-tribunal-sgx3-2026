# persistence helpers
def save_knowledge_base(path: str = OUTPUT_FILE):
    """Write the knowledge base to a JSON file."""
    with open(path, "w", encoding="utf-8") as f:
        json.dump(knowledge_base, f, indent=2, ensure_ascii=False)


def load_knowledge_base(path: str = OUTPUT_FILE) -> dict:
    """Load a previously saved knowledge base (useful for resuming)."""
    with open(path, "r", encoding="utf-8") as f:
        return json.load(f)

"""networkX graph builder"""

# networkX graph builder
def build_graph(kb: dict = None):
    """
    Build a directed NetworkX graph from extracted relationships.

    Each edge carries:  relation, article
    Each node is a concise robotics concept / entity.
    """
    try:
        import networkx as nx
    except ImportError:
        print("networkx not installed — run:  !pip install networkx")
        return None

    if kb is None:
        kb = knowledge_base

    G = nx.DiGraph()
    for rel in kb["relationships"]:
        src = rel.get("source", "").strip()
        tgt = rel.get("target", "").strip()
        if src and tgt:
            G.add_node(src)
            G.add_node(tgt)
            G.add_edge(
                src, tgt,
                relation=rel.get("relation", ""),
                article=rel.get("article", "")
            )

    print(f"Graph built: {G.number_of_nodes()} nodes, {G.number_of_edges()} edges")
    return G


def top_nodes(G, n: int = 20):
    """Print the n most-connected nodes (by total degree)."""
    import networkx as nx
    ranked = sorted(G.degree(), key=lambda x: x[1], reverse=True)[:n]
    print(f"\nTop {n} nodes by degree:")
    for name, deg in ranked:
        print(f"  {deg:>4}  {name}")

